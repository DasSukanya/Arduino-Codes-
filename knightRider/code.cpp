int delayTime = 100;

void setup(){
  pinMode(2, OUTPUT);
  pinMode(3, OUTPUT);
  pinMode(4, OUTPUT);
  pinMode(5, OUTPUT);
  pinMode(6, OUTPUT);
  pinMode(7, OUTPUT);
}
void loop() {
  digitalWrite(2, HIGH);
  delay(delayTime);

  digitalWrite(2, LOW);
  delay(delayTime);

  digitalWrite(3, HIGH);
  delay(delayTime);

  digitalWrite(3, LOW);
  delay(delayTime);

  digitalWrite(4, HIGH);
  delay(delayTime);

  digitalWrite(4, LOW);
  delay(delayTime);

  digitalWrite(5, HIGH);
  delay(delayTime);

  digitalWrite(5, LOW);
  delay(delayTime);

  digitalWrite(6, HIGH);
  delay(delayTime);

  digitalWrite(6, LOW);
  delay(delayTime);

  digitalWrite(7, HIGH);
  delay(delayTime);

  digitalWrite(7, LOW);
  delay(delayTime);

  digitalWrite(6, HIGH);
  delay(delayTime);

  digitalWrite(6, LOW);
  delay(delayTime);

  digitalWrite(5, HIGH);
  delay(delayTime);

  digitalWrite(5, LOW);
  delay(delayTime);

  digitalWrite(4, HIGH);
  delay(delayTime);

  digitalWrite(4, LOW);
  delay(delayTime);

  digitalWrite(3, HIGH);
  delay(delayTime);

  digitalWrite(3, LOW);
  delay(delayTime);

}
