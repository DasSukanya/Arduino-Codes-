int delayTime = 100;

void setup(){
  pinMode(2, OUTPUT);
  pinMode(3, OUTPUT);
  pinMode(4, OUTPUT);
  pinMode(5, OUTPUT);
  pinMode(6, OUTPUT);
  pinMode(7, OUTPUT);
}
void loop() {
  digitalWrite(pin2, HIGH);
  delay(delayTime);

  digitalWrite(pin2, LOW);
  delay(delayTime);

  digitalWrite(pin3, HIGH);
  delay(delayTime);

  digitalWrite(pin3, LOW);
  delay(delayTime);

  digitalWrite(pin4, HIGH);
  delay(delayTime);

  digitalWrite(pin4, LOW);
  delay(delayTime);

  digitalWrite(pin5, HIGH);
  delay(delayTime);

  digitalWrite(pin5, LOW);
  delay(delayTime);

  digitalWrite(pin6, HIGH);
  delay(delayTime);

  digitalWrite(pin6, LOW);
  delay(delayTime);

  digitalWrite(pin7, HIGH);
  delay(delayTime);

  digitalWrite(pin7, LOW);
  delay(delayTime);

  digitalWrite(pin6, HIGH);
  delay(delayTime);

  digitalWrite(pin6, LOW);
  delay(delayTime);

  digitalWrite(pin5, HIGH);
  delay(delayTime);

  digitalWrite(pin5, LOW);
  delay(delayTime);

  digitalWrite(pin4, HIGH);
  delay(delayTime);

  digitalWrite(pin4, LOW);
  delay(delayTime);

  digitalWrite(pin3, HIGH);
  delay(delayTime);

  digitalWrite(pin3, LOW);
  delay(delayTime);

}
